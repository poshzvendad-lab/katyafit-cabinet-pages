// ─── Уровень активности (для бюджета калорий по Миффлину) ──────
// Это БЫТОВАЯ активность вне программы (работа, шаги, прогулки) — тренировки
// программы в подборе уже учтены. Множитель умножает BMR -> TDEE. Описание
// показываем участнице, чтобы выбрала честно (от него зависит дневной бюджет).
// 'medium' = прежний зашитый множитель 1.375 -> у тех, кто не выбирал, бюджет
// не меняется (обратная совместимость). Хранится в строке фазы 'start'.
const ACTIVITY_LEVELS = [
    { key: 'low',    mult: 1.2,   label: 'Низкая',
      desc: 'Сидячий день: офис или учёба, машина, мало хожу пешком.' },
    { key: 'medium', mult: 1.375, label: 'Средняя',
      desc: 'На ногах часть дня: прогулки, бытовые дела, примерно 5–8 тыс. шагов.' },
    { key: 'high',   mult: 1.55,  label: 'Высокая',
      desc: 'Много двигаюсь весь день: активная работа, 10 тыс.+ шагов или спорт помимо программы.' },
];
const ACTIVITY_DEFAULT = 'medium';
function activityLevel(key) {
    return ACTIVITY_LEVELS.find(l => l.key === key)
        || ACTIVITY_LEVELS.find(l => l.key === ACTIVITY_DEFAULT);
}
function activityMult(key) { return activityLevel(key).mult; }

// ─── Скелет 28 дней (без контента) ───────────────────────────
function buildManifest() {
    const days = [];
    for (let i = 1; i <= 28; i++) {
        const dow = ((i - 1) % 7) + 1; // 1..7 внутри недели
        const type = (dow === 1 || dow === 3 || dow === 5) ? 'workout'
                   : (dow === 7) ? 'rest' : 'recovery';
        days.push({ n: i, week: Math.ceil(i / 7), type });
    }
    return days;
}

const PROGRAM = buildManifest();

